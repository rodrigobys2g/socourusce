<?php
class RedFeet_Checkout_Block_Cart_Totals extends Mage_Checkout_Block_Cart_Totals
{
    public function renderTotal($total, $area = null, $colspan = 1)
    {
        $code = $total->getCode();
        if ($total->getAs()) {
            $code = $total->getAs();
        }
        if($code == 'subtotal') return '';
        return $this->_getTotalRenderer($code)
            ->setTotal($total)
            ->setColspan($colspan)
            ->setRenderingArea(is_null($area) ? -1 : $area)
            ->toHtml();
    }
}
